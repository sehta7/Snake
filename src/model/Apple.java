package model;

public class Apple {

	private int x;
	private int y;
	private int diameter;
	
	public Apple() {
		this.diameter = 10;
	}
	
	public int getX() {
		return x;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public int getY() {
		return y;
	}
	
	public void setY(int y) {
		this.y = y;
	}

	public int getDiameter() {
		return diameter;
	}
	
}
